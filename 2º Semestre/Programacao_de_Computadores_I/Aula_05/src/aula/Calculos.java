package aula;

public class Calculos {
	public int somar(int a, int b){
		int total = a + b;
		return total;
	}
	
	public double multiplicar(int a, int b){
		int total = a * b;
		return total;
	}
	
	public double dividir(int a, int b){
		int total = a / b;
		return total;
	}
	
	public double subtrair(int a, int b){
		int total = a - b;
		return total;
	}
}
