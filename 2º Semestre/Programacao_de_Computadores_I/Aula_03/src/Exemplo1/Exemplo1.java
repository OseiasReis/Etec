package Exemplo1;

public class Exemplo1 {
	public static void main( String[] args){
		int n1 = 1;
		
		//Pré-incremento
		System.out.println(++n1);
		
		//Pós-incremento
		System.out.println(n1++); 
		
		//Pós-decremento
		System.out.println(n1--);
		
		//Pré-decremento
		System.out.println(--n1);
	}
}
